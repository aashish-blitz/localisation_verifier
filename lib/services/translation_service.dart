  static List<TranslationEntry> getAllTranslations() {
    final List<TranslationEntry> allEntries = [];

    for (final feature in _getFeatureTranslations()) {
      final translationsByLang = feature.translationsByLanguage;

      // Get all keys from the English translation (or first available)
      final enMap = translationsByLang['en'] ?? translationsByLang.values.first;
      final allKeys = enMap.keys.toList()..sort();

      for (final key in allKeys) {
        final translations = <String, String>{};

        // Get translation for each language
        for (final langCode in languageNames.keys) {
          translations[langCode] = translationsByLang[langCode]?[key] ?? '';
        }

        allEntries.add(
          TranslationEntry(
        key: key,
            feature: feature.featureName,
            translations: translations,
          ),
        );
      }
    }

    return allEntries;
  }

  /// Get all unique feature names
  static List<String> getFeatures() {
    return _getFeatureTranslations()
        .map((feature) => feature.featureName)
        .toList()
      ..sort();
  }

  /// Get translations grouped by feature
  static Map<String, List<TranslationEntry>> getTranslationsByFeature() {
    final translations = getAllTranslations();
    final Map<String, List<TranslationEntry>> grouped = {};

    for (final entry in translations) {
      if (!grouped.containsKey(entry.feature)) {
        grouped[entry.feature] = [];
      }
      grouped[entry.feature]!.add(entry);
    }

    // Sort entries within each feature
    for (final feature in grouped.keys) {
      grouped[feature]!.sort((a, b) => a.key.compareTo(b.key));
    }

    return grouped;
  }

  static String getLanguageName(String code) {
    return languageNames[code] ?? code;
  }
